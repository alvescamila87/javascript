// split: divide uma string por separador e retorna um array

let frase = "Só sei que nada sei.";
console.log(frase.split(" "));

let palavras = frase.split(" "); // é possível fazer dessa forma também
console.log(palavras);

let produtos = "Banana;Maça;Pera;Abacaxi;Bola;Tapete";
console.log(produtos.split(";"));
